package com.veeparch.bean;

public class category {
    private int id;
    private String 类别;
    private String 中文含义;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String get类别() {
        return 类别;
    }

    public void set类别(String 类别) {
        this.类别 = 类别;
    }

    public String get中文含义() {
        return 中文含义;
    }

    public void set中文含义(String 中文含义) {
        this.中文含义 = 中文含义;
    }

    public String get英文含义() {
        return 英文含义;
    }

    public void set英文含义(String 英文含义) {
        this.英文含义 = 英文含义;
    }

    public String get西语含义() {
        return 西语含义;
    }

    public void set西语含义(String 西语含义) {
        this.西语含义 = 西语含义;
    }

    public String get俄语含义() {
        return 俄语含义;
    }

    public void set俄语含义(String 俄语含义) {
        this.俄语含义 = 俄语含义;
    }

    private String 英文含义;
    private String 西语含义;
    private String 俄语含义;


}
