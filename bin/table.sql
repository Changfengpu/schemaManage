	`line_hash` VARCHAR(100) NULL DEFAULT NULL,
	`slot` VARCHAR(100) NULL DEFAULT NULL,
	`line_sentiment` VARCHAR(100) NULL DEFAULT NULL,
	`is_keeped` TINYINT(1) NULL DEFAULT NULL,
