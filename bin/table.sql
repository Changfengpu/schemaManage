	`subject_index` INT(11) NULL DEFAULT NULL,
	`predicate_index` INT(11) NULL DEFAULT NULL,
	`object_index` INT(11) NULL DEFAULT NULL,
	`user` VARCHAR(50) NULL DEFAULT NULL,
