	`subject` VARCHAR(200) NULL DEFAULT NULL,
	`predicate` VARCHAR(200) NULL DEFAULT NULL,
	`object` VARCHAR(200) NULL DEFAULT NULL,
	`sentiment` VARCHAR(500) NULL DEFAULT NULL,
	`line_segment` LONGTEXT NULL,
